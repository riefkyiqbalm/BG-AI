    def estimate_runtime(num_files, num_mach, num_re, time_per_run):
        # total kombinasi per file
        combinations = num_mach * num_re
        # total run keseluruhan
        total_runs = num_files * combinations
        # total waktu (detik)
        total_time_seconds = total_runs * time_per_run

        # konversi ke menit dan detik
        minutes = total_time_seconds // 60
        seconds = total_time_seconds % 60

        return total_runs, total_time_seconds, f"{minutes} menit {seconds} detik"


